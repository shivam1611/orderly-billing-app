//  menu items reducer

export default function menu_reducer(state = initialState, action) {
  switch (action.type) {
    case "menu/add_menu_item":
      return { ...state, items: [action.payload, ...state.items] };
    case "menu/remove_menu_item":
      return {
        ...state,
        items: state.items.filter((food) => food.food_id !== action.payload),
      };
    default:
      return state;
  }
}

// action creators functions

export function add_menu_item(obj) {
  return { type: "menu/add_menu_item", payload: obj };
}

export function remove_menu_item(id) {
  return { type: "menu/remove_item", payload: id };
}
